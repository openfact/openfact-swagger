package org.openfact.rest.idm;

import java.math.BigDecimal;

public class DocumentLineRequestRepresentation {

    protected String unitCode;
    protected BigDecimal cantidad;
    protected String tipoDeIgv;
    protected BigDecimal valorUnitario;
    protected BigDecimal precioUnitario;
    protected BigDecimal subtotal;
    protected BigDecimal igv;
    protected BigDecimal total;
    protected String descripcion;

    public String getUnitCode() {
        return unitCode;
    }

    public void setUnitCode(String unitCode) {
        this.unitCode = unitCode;
    }

    public BigDecimal getCantidad() {
        return cantidad;
    }

    public void setCantidad(BigDecimal cantidad) {
        this.cantidad = cantidad;
    }

    public String getTipoDeIgv() {
        return tipoDeIgv;
    }

    public void setTipoDeIgv(String tipoDeIgv) {
        this.tipoDeIgv = tipoDeIgv;
    }

    public BigDecimal getValorUnitario() {
        return valorUnitario;
    }

    public void setValorUnitario(BigDecimal valorUnitario) {
        this.valorUnitario = valorUnitario;
    }

    public BigDecimal getPrecioUnitario() {
        return precioUnitario;
    }

    public void setPrecioUnitario(BigDecimal precioUnitario) {
        this.precioUnitario = precioUnitario;
    }

    public BigDecimal getSubtotal() {
        return subtotal;
    }

    public void setSubtotal(BigDecimal subtotal) {
        this.subtotal = subtotal;
    }

    public BigDecimal getIgv() {
        return igv;
    }

    public void setIgv(BigDecimal igv) {
        this.igv = igv;
    }

    public BigDecimal getTotal() {
        return total;
    }

    public void setTotal(BigDecimal total) {
        this.total = total;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

}